#include "libft.h"

int	ft_atoi(const char *str)
{
	int	negative;
	int	rep;
	int	i;

	negative = 1;
	rep = 0;
	i = 0;
	while (str[i] && (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
			|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			negative *= -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		rep *= 10;
		rep += str[i] - 48;
		i++;
	}
	return (rep * negative);
}
